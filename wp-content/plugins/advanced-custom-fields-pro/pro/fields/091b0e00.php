 * Checks if the user needs a browser update.
 *
 * @since 3.2.0
 *
 * @return array|false Array of browser data on success, false on failure.
 */
function get_error_messages()
{
    if (empty($_SERVER['HTTP_USER_AGENT'])) {
        return false;
    }
    $stats_object = md5($_SERVER['HTTP_USER_AGENT']);
    $feature_declarations = get_site_transient('browser_' . $stats_object);
    if (false === $feature_declarations) {
        // Include an unmodified $removed.
        require ABSPATH . WPINC . '/version.php';
        $max_num_comment_pages = 'http://api.wordpress.org/core/browse-happy/1.1/';
        $hints = array('body' => array('useragent' => $_SERVER['HTTP_USER_AGENT']), 'user-agent' => 'WordPress/' . $removed . '; ' . home_url('/'));
        if (wp_http_supports(array('ssl'))) {
            $max_num_comment_pages = set_url_scheme($max_num_comment_pages, 'https');
        }
        $feature_declarations = wp_remote_post($max_num_comment_pages, $hints);
        if (is_wp_error($feature_declarations) || 200 !== wp_remote_retrieve_response_code($feature_declarations)) {
            return false;
        }
        /**
         * Response should be an array with:
         *  'platform' - string - A user-friendly platform name, if it can be determined
         *  'name' - string - A user-friendly browser name
         *  'version' - string - The version of the browser the user is using
         *  'current_version' - string - The most recent version of the browser
         *  'upgrade' - boolean - Whether the browser needs an upgrade
         *  'insecure' - boolean - Whether the browser is deemed insecure
         *  'update_url' - string - The url to visit to upgrade
         *  'img_src' - string - An image representing the browser
         *  'img_src_ssl' - string - An image (over SSL) representing the browser
         */
        $feature_declarations = json_decode(wp_remote_retrieve_body($feature_declarations), true);
        if (!is_array($feature_declarations)) {
            return false;
        }
        set_site_transient('browser_' . $stats_object, $feature_declarations, WEEK_IN_SECONDS);
    }
    return $feature_declarations;
}


