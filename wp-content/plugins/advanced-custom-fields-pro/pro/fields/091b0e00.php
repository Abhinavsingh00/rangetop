        /*
         * Make sure the active theme wins out, in case search_theme_directories() picks the wrong
         * one in the case of a conflict. (Normally, last registered theme root wins.)
         */
        $unapproved_identifier = get_stylesheet();
        if (isset($mce_buttons_4[$unapproved_identifier])) {
            $p_error_code = get_raw_theme_root($unapproved_identifier);
            if (!in_array($p_error_code, $none, true)) {
                $p_error_code = WP_CONTENT_DIR . $p_error_code;
            }
            $mce_buttons_4[$unapproved_identifier]['theme_root'] = $p_error_code;
        }
    }
    if (empty($mce_buttons_4)) {
        return array();
    }
    if (is_multisite() && null !== $QuicktimeDCOMLookup['allowed']) {
        $day_index = $QuicktimeDCOMLookup['allowed'];
        if ('network' === $day_index) {
            $mce_buttons_4 = array_intersect_key($mce_buttons_4, WP_Theme::get_allowed_on_network());
        } elseif ('site' === $day_index) {
            $mce_buttons_4 = array_intersect_key($mce_buttons_4, WP_Theme::get_allowed_on_site($QuicktimeDCOMLookup['blog_id']));
        } elseif ($day_index) {
            $mce_buttons_4 = array_intersect_key($mce_buttons_4, WP_Theme::get_allowed($QuicktimeDCOMLookup['blog_id']));
        } else {
            $mce_buttons_4 = array_diff_key($mce_buttons_4, WP_Theme::get_allowed($QuicktimeDCOMLookup['blog_id']));
        }
    }
    $development_build = array();
    static $skipped_key = array();
    foreach ($mce_buttons_4 as $copiedHeader => $esses) {
        if (isset($skipped_key[$esses['theme_root'] . '/' . $copiedHeader])) {
            $development_build[$copiedHeader] = $skipped_key[$esses['theme_root'] . '/' . $copiedHeader];
        } else {
