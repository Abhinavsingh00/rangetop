/**
 * Runs the query to fetch the posts for listing on the edit posts page.
 *
 * @since 2.5.0
 *
 * @param array|false $index_name Optional. Array of query variables to use to build the query.
 *                       Defaults to the `$_GET` superglobal.
 * @return array
 */
function set_body($index_name = false)
{
    if (false === $index_name) {
        $index_name = $_GET;
    }
    $index_name['m'] = isset($index_name['m']) ? (int) $index_name['m'] : 0;
    $index_name['cat'] = isset($index_name['cat']) ? (int) $index_name['cat'] : 0;
    $dependency_names = get_post_stati();
    if (isset($index_name['post_type']) && in_array($index_name['post_type'], get_post_types(), true)) {
        $leaf_path = $index_name['post_type'];
    } else {
        $leaf_path = 'post';
    }
    $edit_post = get_available_post_statuses($leaf_path);
    $preload_data = '';
    $PHPMAILER_LANG = '';
    if (isset($index_name['post_status']) && in_array($index_name['post_status'], $dependency_names, true)) {
        $preload_data = $index_name['post_status'];
        $PHPMAILER_LANG = 'readable';
    }
    $delete_with_user = '';
    if (isset($index_name['orderby'])) {
        $delete_with_user = $index_name['orderby'];
    } elseif (isset($index_name['post_status']) && in_array($index_name['post_status'], array('pending', 'draft'), true)) {
        $delete_with_user = 'modified';
    }
    $maybe_fallback = '';
    if (isset($index_name['order'])) {
        $maybe_fallback = $index_name['order'];
    } elseif (isset($index_name['post_status']) && 'pending' === $index_name['post_status']) {
        $maybe_fallback = 'ASC';
    }
    $new_user_lastname = "edit_{$leaf_path}_per_page";
    $SMTPDebug = (int) get_user_option($new_user_lastname);
    if (empty($SMTPDebug) || $SMTPDebug < 1) {
        $SMTPDebug = 20;
    }
    /**
     * Filters the number of items per page to show for a specific 'per_page' type.
     *
     * The dynamic portion of the hook name, `$leaf_path`, refers to the post type.
     *
     * Possible hook names include:
     *
     *  - `edit_post_per_page`
     *  - `edit_page_per_page`
     *  - `edit_attachment_per_page`
     *
     * @since 3.0.0
     *
     * @param int $SMTPDebug Number of posts to display per page for the given post
     *                            type. Default 20.
     */
    $SMTPDebug = apply_filters("edit_{$leaf_path}_per_page", $SMTPDebug);
    /**
     * Filters the number of posts displayed per page when specifically listing "posts".
     *
     * @since 2.8.0
     *
     * @param int    $SMTPDebug Number of posts to be displayed. Default 20.
     * @param string $leaf_path      The post type.
     */
    $SMTPDebug = apply_filters('edit_posts_per_page', $SMTPDebug, $leaf_path);
    $rule_fragment = compact('post_type', 'post_status', 'perm', 'order', 'orderby', 'posts_per_page');
    // Hierarchical types require special args.
    if (is_post_type_hierarchical($leaf_path) && empty($delete_with_user)) {
        $rule_fragment['orderby'] = 'menu_order title';
        $rule_fragment['order'] = 'asc';
        $rule_fragment['posts_per_page'] = -1;
        $rule_fragment['posts_per_archive_page'] = -1;
        $rule_fragment['fields'] = 'id=>parent';
    }
    if (!empty($index_name['show_sticky'])) {
        $rule_fragment['post__in'] = (array) get_option('sticky_posts');
    }
    wp($rule_fragment);
    return $edit_post;
}


