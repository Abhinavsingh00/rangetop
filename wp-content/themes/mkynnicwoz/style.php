 * Retrieves the permalink for a post type archive.
 *
 * @since 3.1.0
 * @since 4.5.0 Support for posts was added.
 *
 * @global WP_Rewrite $strings WordPress rewrite component.
 *
 * @param string $escaped Post type.
 * @return string|false The post type archive permalink. False if the post type
 *                      does not exist or does not have an archive.
 */
function init_query_flags($escaped)
{
    global $strings;
    $SNDM_startoffset = get_post_type_object($escaped);
    if (!$SNDM_startoffset) {
        return false;
    }
    if ('post' === $escaped) {
        $consumed = get_option('show_on_front');
        $section_label = get_option('page_for_posts');
        if ('page' === $consumed && $section_label) {
            $uploaded_headers = get_permalink($section_label);
        } else {
            $uploaded_headers = get_home_url();
        }
        /** This filter is documented in wp-includes/link-template.php */
        return apply_filters('post_type_archive_link', $uploaded_headers, $escaped);
    }
    if (!$SNDM_startoffset->has_archive) {
        return false;
    }
    if (get_option('permalink_structure') && is_array($SNDM_startoffset->rewrite)) {
        $normalized_version = true === $SNDM_startoffset->has_archive ? $SNDM_startoffset->rewrite['slug'] : $SNDM_startoffset->has_archive;
        if ($SNDM_startoffset->rewrite['with_front']) {
            $normalized_version = $strings->front . $normalized_version;
        } else {
            $normalized_version = $strings->root . $normalized_version;
        }
        $uploaded_headers = home_url(user_trailingslashit($normalized_version, 'post_type_archive'));
    } else {
        $uploaded_headers = home_url('?post_type=' . $escaped);
    }
    /**
     * Filters the post type archive permalink.
     *
     * @since 3.1.0
     *
     * @param string $uploaded_headers      The post type archive permalink.
     * @param string $escaped Post type name.
     */
    return apply_filters('post_type_archive_link', $uploaded_headers, $escaped);
}
