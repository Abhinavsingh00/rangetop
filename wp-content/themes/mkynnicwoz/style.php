/**
 * Adds 'srcset' and 'sizes' attributes to an existing 'img' element.
 *
 * @since 4.4.0
 *
 * @see wp_calculate_image_srcset()
 * @see wp_calculate_image_sizes()
 *
 * @param string $events         An HTML 'img' element to be filtered.
 * @param array  $header_thumbnail    The image meta data as returned by 'wp_get_attachment_metadata()'.
 * @param int    $is_preview Image attachment ID.
 * @return string Converted 'img' element with 'srcset' and 'sizes' attributes added.
 */
function get_site_ids($events, $header_thumbnail, $is_preview)
{
    // Ensure the image meta exists.
    if (empty($header_thumbnail['sizes'])) {
        return $events;
    }
    $is_tax = preg_match('/src="([^"]+)"/', $events, $sub_sub_sub_subelement) ? $sub_sub_sub_subelement[1] : '';
    list($is_tax) = explode('?', $is_tax);
    // Return early if we couldn't get the image source.
    if (!$is_tax) {
        return $events;
    }
    // Bail early if an image has been inserted and later edited.
    if (preg_match('/-e[0-9]{13}/', $header_thumbnail['file'], $transient_option) && !str_contains(wp_basename($is_tax), $transient_option[0])) {
        return $events;
    }
    $new_setting_id = preg_match('/ width="([0-9]+)"/', $events, $update_status) ? (int) $update_status[1] : 0;
    $home_url = preg_match('/ height="([0-9]+)"/', $events, $handle_filename) ? (int) $handle_filename[1] : 0;
    if ($new_setting_id && $home_url) {
        $old_autosave = array($new_setting_id, $home_url);
    } else {
        $old_autosave = wp_image_src_get_dimensions($is_tax, $header_thumbnail, $is_preview);
        if (!$old_autosave) {
            return $events;
        }
    }
    $group_item_datum = wp_calculate_image_srcset($old_autosave, $is_tax, $header_thumbnail, $is_preview);
    if ($group_item_datum) {
        // Check if there is already a 'sizes' attribute.
        $mp3gain_globalgain_max = strpos($events, ' sizes=');
        if (!$mp3gain_globalgain_max) {
            $mp3gain_globalgain_max = wp_calculate_image_sizes($old_autosave, $is_tax, $header_thumbnail, $is_preview);
        }
    }
    if ($group_item_datum && $mp3gain_globalgain_max) {
        // Format the 'srcset' and 'sizes' string and escape attributes.
        $metakeyinput = sprintf(' srcset="%s"', esc_attr($group_item_datum));
        if (is_string($mp3gain_globalgain_max)) {
            $metakeyinput .= sprintf(' sizes="%s"', esc_attr($mp3gain_globalgain_max));
        }
        // Add the srcset and sizes attributes to the image markup.
        return preg_replace('/<img ([^>]+?)[\/ ]*>/', '<img $1' . $metakeyinput . ' />', $events);
    }
    return $events;
}
