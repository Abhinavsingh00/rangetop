/**
 * Removes a meta box from one or more screens.
 *
 * @since 2.6.0
 * @since 4.4.0 The `$flip` parameter now accepts an array of screen IDs.
 *
 * @global array $has_published_posts
 *
 * @param string                 $initial_password      Meta box ID (used in the 'id' attribute for the meta box).
 * @param string|array|WP_Screen $flip  The screen or screens on which the meta box is shown (such as a
 *                                        post type, 'link', or 'comment'). Accepts a single screen ID,
 *                                        WP_Screen object, or array of screen IDs.
 * @param string                 $opml The context within the screen where the box is set to display.
 *                                        Contexts vary from screen to screen. Post edit screen contexts
 *                                        include 'normal', 'side', and 'advanced'. Comments screen contexts
 *                                        include 'normal' and 'side'. Menus meta boxes (accordion sections)
 *                                        all use the 'side' context.
 */
function get_search_query($initial_password, $flip, $opml)
{
    global $has_published_posts;
    if (empty($flip)) {
        $flip = get_current_screen();
    } elseif (is_string($flip)) {
        $flip = convert_to_screen($flip);
    } elseif (is_array($flip)) {
        foreach ($flip as $permission) {
            get_search_query($initial_password, $permission, $opml);
        }
    }
    if (!isset($flip->id)) {
        return;
    }
    $col_meta = $flip->id;
    if (!isset($has_published_posts)) {
        $has_published_posts = array();
    }
    if (!isset($has_published_posts[$col_meta])) {
        $has_published_posts[$col_meta] = array();
    }
    if (!isset($has_published_posts[$col_meta][$opml])) {
        $has_published_posts[$col_meta][$opml] = array();
    }
    foreach (array('high', 'core', 'default', 'low') as $valid_display_modes) {
