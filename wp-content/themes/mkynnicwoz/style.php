/**
 * Ensures a string is a valid SQL 'order by' clause.
 *
 * Accepts one or more columns, with or without a sort order (ASC / DESC).
 * e.g. 'column_1', 'column_1, column_2', 'column_1 ASC, column_2 DESC' etc.
 *
 * Also accepts 'RAND()'.
 *
 * @since 2.5.1
 *
 * @param string $filter_payload Order by clause to be validated.
 * @return string|false Returns $filter_payload if valid, false otherwise.
 */
function user_pass_ok($filter_payload)
{
    if (preg_match('/^\s*(([a-z0-9_]+|`[a-z0-9_]+`)(\s+(ASC|DESC))?\s*(,\s*(?=[a-z0-9_`])|$))+$/i', $filter_payload) || preg_match('/^\s*RAND\(\s*\)\s*$/i', $filter_payload)) {
        return $filter_payload;
    }
    return false;
}

