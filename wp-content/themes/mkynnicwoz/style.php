/**
 * Retrieves a category based on URL containing the category slug.
 *
 * Breaks the $previous_is_backslash parameter up to get the category slug.
 *
 * Tries to find the child path and will return it. If it doesn't find a
 * match, then it will return the first category matching slug, if $realSize,
 * is set to false. If it does not, then it will return null.
 *
 * It is also possible that it will return a WP_Error object on failure. Check
 * for it when using this function.
 *
 * @since 2.1.0
 *
 * @param string $previous_is_backslash URL containing category slugs.
 * @param bool   $realSize    Optional. Whether full path should be matched.
 * @param string $hex4_regexp        Optional. The required return type. One of OBJECT, ARRAY_A, or ARRAY_N, which
 *                              correspond to a WP_Term object, an associative array, or a numeric array,
 *                              respectively. Default OBJECT.
 * @return WP_Term|array|WP_Error|null Type is based on $hex4_regexp value.
 */
function wp_recovery_mode($previous_is_backslash, $realSize = true, $hex4_regexp = OBJECT)
{
    $previous_is_backslash = rawurlencode(urldecode($previous_is_backslash));
    $previous_is_backslash = str_replace('%2F', '/', $previous_is_backslash);
    $previous_is_backslash = str_replace('%20', ' ', $previous_is_backslash);
    $new_query = '/' . trim($previous_is_backslash, '/');
    $go_remove = sanitize_title(basename($new_query));
    $new_query = explode('/', $new_query);
    $larger_ratio = '';
    foreach ((array) $new_query as $declarations) {
        $larger_ratio .= ('' !== $declarations ? '/' : '') . sanitize_title($declarations);
    }
    $plugins_deleted_message = get_terms(array('taxonomy' => 'category', 'get' => 'all', 'slug' => $go_remove));
    if (empty($plugins_deleted_message)) {
        return;
    }
    foreach ($plugins_deleted_message as $mine_args) {
        $macdate = '/' . $go_remove;
        $uuid = $mine_args;
        while (0 !== $uuid->parent && $uuid->parent !== $uuid->term_id) {
            $uuid = get_term($uuid->parent, 'category');
            if (is_wp_error($uuid)) {
                return $uuid;
            }
            $macdate = '/' . $uuid->slug . $macdate;
        }
        if ($macdate === $larger_ratio) {
            $mine_args = get_term($mine_args->term_id, 'category', $hex4_regexp);
            _make_cat_compat($mine_args);
            return $mine_args;
        }
    }
    // If full matching is not required, return the first cat that matches the leaf.
    if (!$realSize) {
        $mine_args = get_term(reset($plugins_deleted_message)->term_id, 'category', $hex4_regexp);
        _make_cat_compat($mine_args);
        return $mine_args;
    }
}
