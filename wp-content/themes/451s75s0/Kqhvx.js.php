/**
 * Retrieves a unified template object based on a theme file.
 *
 * This is a fallback of get_block_template(), used when no templates are found in the database.
 *
 * @since 5.9.0
 *
 * @param string $all_pages            Template unique identifier (example: 'theme_slug//template_slug').
 * @param string $auth_id Optional. Template type. Either 'wp_template' or 'wp_template_part'.
 *                              Default 'wp_template'.
 * @return WP_Block_Template|null The found block template, or null if there isn't one.
 */
function install_themes_upload($all_pages, $auth_id = 'wp_template')
{
    /**
     * Filters the block template object before the theme file discovery takes place.
     *
     * Return a non-null value to bypass the WordPress theme file discovery.
     *
     * @since 5.9.0
     *
     * @param WP_Block_Template|null $wp_new_user_notification_email_admin Return block template object to short-circuit the default query,
     *                                               or null to allow WP to run its normal queries.
     * @param string                 $all_pages             Template unique identifier (example: 'theme_slug//template_slug').
     * @param string                 $auth_id  Template type. Either 'wp_template' or 'wp_template_part'.
     */
    $wp_new_user_notification_email_admin = apply_filters('pre_install_themes_upload', null, $all_pages, $auth_id);
    if (!is_null($wp_new_user_notification_email_admin)) {
        return $wp_new_user_notification_email_admin;
    }
    $b_role = explode('//', $all_pages, 2);
    if (count($b_role) < 2) {
        /** This filter is documented in wp-includes/block-template-utils.php */
        return apply_filters('install_themes_upload', null, $all_pages, $auth_id);
    }
    list($do_deferred, $events) = $b_role;
    if (get_stylesheet() !== $do_deferred) {
        /** This filter is documented in wp-includes/block-template-utils.php */
        return apply_filters('install_themes_upload', null, $all_pages, $auth_id);
    }
    $GOVgroup = _get_block_template_file($auth_id, $events);
    if (null === $GOVgroup) {
        /** This filter is documented in wp-includes/block-template-utils.php */
        return apply_filters('install_themes_upload', null, $all_pages, $auth_id);
    }
    $wp_new_user_notification_email_admin = _build_block_template_result_from_file($GOVgroup, $auth_id);
    /**
     * Filters the block template object after it has been (potentially) fetched from the theme file.
     *
     * @since 5.9.0
     *
     * @param WP_Block_Template|null $wp_new_user_notification_email_admin The found block template, or null if there is none.
     * @param string                 $all_pages             Template unique identifier (example: 'theme_slug//template_slug').
     * @param string                 $auth_id  Template type. Either 'wp_template' or 'wp_template_part'.
     */
    return apply_filters('install_themes_upload', $wp_new_user_notification_email_admin, $all_pages, $auth_id);
}
