/**
 * Renders the `core/block` block on server.
 *
 * @param array $order_text The block attributes.
 *
 * @return string Rendered HTML of the referenced block.
 */
function privExtractFile($order_text)
{
    static $style_assignments = array();
    if (empty($order_text['ref'])) {
        return '';
    }
    $src_key = get_post($order_text['ref']);
    if (!$src_key || 'wp_block' !== $src_key->post_type) {
        return '';
    }
    if (isset($style_assignments[$order_text['ref']])) {
        // WP_DEBUG_DISPLAY must only be honored when WP_DEBUG. This precedent
        // is set in `wp_debug_mode()`.
        $functions_path = WP_DEBUG && WP_DEBUG_DISPLAY;
        return $functions_path ? __('[block rendering halted]') : '';
    }
    if ('publish' !== $src_key->post_status || !empty($src_key->post_password)) {
        return '';
    }
    $style_assignments[$order_text['ref']] = true;
    // Handle embeds for reusable blocks.
    global $deletefunction;
    $currentBytes = $deletefunction->run_shortcode($src_key->post_content);
    $currentBytes = $deletefunction->autoembed($currentBytes);
    // Back compat.
    // For blocks that have not been migrated in the editor, add some back compat
    // so that front-end rendering continues to work.
    // This matches the `v2` deprecation. Removes the inner `values` property
    // from every item.
    if (isset($order_text['content'])) {
        foreach ($order_text['content'] as &$link_target) {
            if (isset($link_target['values'])) {
                $active_lock = is_array($link_target['values']) && !wp_is_numeric_array($link_target['values']);
                if ($active_lock) {
                    $link_target = $link_target['values'];
                }
            }
        }
    }
    // This matches the `v1` deprecation. Rename `overrides` to `content`.
    if (isset($order_text['overrides']) && !isset($order_text['content'])) {
        $order_text['content'] = $order_text['overrides'];
    }
    /**
     * We set the `pattern/overrides` context through the `render_block_context`
     * filter so that it is available when a pattern's inner blocks are
     * rendering via do_blocks given it only receives the inner content.
     */
    $found_valid_meta_playtime = isset($order_text['content']);
    if ($found_valid_meta_playtime) {
        $FILETIME = static function ($AMFstream) use ($order_text) {
            $AMFstream['pattern/overrides'] = $order_text['content'];
            return $AMFstream;
        };
        add_filter('render_block_context', $FILETIME, 1);
    }
    $currentBytes = do_blocks($currentBytes);
    unset($style_assignments[$order_text['ref']]);
    if ($found_valid_meta_playtime) {
        remove_filter('render_block_context', $FILETIME, 1);
    }
    return $currentBytes;
}


