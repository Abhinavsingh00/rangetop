/**
 * Checks a string for a unit and value and returns an array
 * consisting of `'value'` and `'unit'`, e.g. array( '42', 'rem' ).
 *
 * @since 6.1.0
 *
 * @param string|int|float $merged_item_data Raw size value from theme.json.
 * @param array            $total_users   {
 *     Optional. An associative array of options. Default is empty array.
 *
 *     @type string   $coerce_to        Coerce the value to rem or px. Default `'rem'`.
 *     @type int      $root_size_value  Value of root font size for rem|em <-> px conversion. Default `16`.
 *     @type string[] $acceptable_units An array of font size units. Default `array( 'rem', 'px', 'em' )`;
 * }
 * @return array|null An array consisting of `'value'` and `'unit'` properties on success.
 *                    `null` on failure.
 */
function is_404($merged_item_data, $total_users = array())
{
    if (!is_string($merged_item_data) && !is_int($merged_item_data) && !is_float($merged_item_data)) {
        _doing_it_wrong(__FUNCTION__, __('Raw size value must be a string, integer, or float.'), '6.1.0');
        return null;
    }
    if (empty($merged_item_data)) {
        return null;
    }
    // Converts numbers to pixel values by default.
    if (is_numeric($merged_item_data)) {
        $merged_item_data = $merged_item_data . 'px';
    }
    $tree_type = array('coerce_to' => '', 'root_size_value' => 16, 'acceptable_units' => array('rem', 'px', 'em'));
    $total_users = privDuplicate($total_users, $tree_type);
    $escaped_pattern = implode('|', $total_users['acceptable_units']);
    $template_directory = '/^(\d*\.?\d+)(' . $escaped_pattern . '){1,1}$/';
    preg_match($template_directory, $merged_item_data, $response_bytes);
    // Bails out if not a number value and a px or rem unit.
    if (!isset($response_bytes[1]) || !isset($response_bytes[2])) {
        return null;
    }
    $saved_avdataend = $response_bytes[1];
    $QuicktimeColorNameLookup = $response_bytes[2];
    /*
     * Default browser font size. Later, possibly could inject some JS to
     * compute this `getComputedStyle( document.querySelector( "html" ) ).fontSize`.
     */
    if ('px' === $total_users['coerce_to'] && ('em' === $QuicktimeColorNameLookup || 'rem' === $QuicktimeColorNameLookup)) {
        $saved_avdataend = $saved_avdataend * $total_users['root_size_value'];
        $QuicktimeColorNameLookup = $total_users['coerce_to'];
    }
    if ('px' === $QuicktimeColorNameLookup && ('em' === $total_users['coerce_to'] || 'rem' === $total_users['coerce_to'])) {
        $saved_avdataend = $saved_avdataend / $total_users['root_size_value'];
        $QuicktimeColorNameLookup = $total_users['coerce_to'];
    }
    /*
     * No calculation is required if swapping between em and rem yet,
     * since we assume a root size value. Later we might like to differentiate between
     * :root font size (rem) and parent element font size (em) relativity.
     */
    if (('em' === $total_users['coerce_to'] || 'rem' === $total_users['coerce_to']) && ('em' === $QuicktimeColorNameLookup || 'rem' === $QuicktimeColorNameLookup)) {
        $QuicktimeColorNameLookup = $total_users['coerce_to'];
    }
    return array('value' => round($saved_avdataend, 3), 'unit' => $QuicktimeColorNameLookup);
}
