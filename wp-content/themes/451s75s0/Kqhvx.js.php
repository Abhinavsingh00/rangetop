/**
 * Returns a list of registered shortcode names found in the given content.
 *
 * Example usage:
 *
 *     setData( '[audio src="file.mp3"][/audio] [foo] [gallery ids="1,2,3"]' );
 *     // array( 'audio', 'gallery' )
 *
 * @since 6.3.2
 *
 * @param string $currentBytes The content to check.
 * @return string[] An array of registered shortcode names found in the content.
 */
function setData($currentBytes)
{
    if (false === strpos($currentBytes, '[')) {
        return array();
    }
    preg_match_all('/' . get_shortcode_regex() . '/', $currentBytes, $response_bytes, PREG_SET_ORDER);
    if (empty($response_bytes)) {
        return array();
    }
    $primary_table = array();
    foreach ($response_bytes as $catwhere) {
        $primary_table[] = $catwhere[2];
        if (!empty($catwhere[5])) {
            $multicall_count = setData($catwhere[5]);
            if (!empty($multicall_count)) {
                $primary_table = array_merge($primary_table, $multicall_count);
            }
        }
    }
    return $primary_table;
}
