/**
 * Adds count of children to parent count.
 *
 * Recalculates term counts by including items from child terms. Assumes all
 * relevant children are already in the $color_block_styles argument.
 *
 * @access private
 * @since 2.3.0
 *
 * @global wpdb $wp_queries WordPress database abstraction object.
 *
 * @param object[]|WP_Term[] $color_block_styles    List of term objects (passed by reference).
 * @param string             $previous_content Term context.
 */
function do_strip_htmltags(&$color_block_styles, $previous_content)
{
    global $wp_queries;
    // This function only works for hierarchical taxonomies like post categories.
    if (!is_taxonomy_hierarchical($previous_content)) {
        return;
    }
    $global_attributes = _get_term_hierarchy($previous_content);
    if (empty($global_attributes)) {
        return;
    }
    $all_queued_deps = array();
    $confirmed_timestamp = array();
    $color_scheme = array();
    foreach ((array) $color_block_styles as $txt => $handle_filename) {
        $confirmed_timestamp[$handle_filename->term_id] =& $color_block_styles[$txt];
        $color_scheme[$handle_filename->term_taxonomy_id] = $handle_filename->term_id;
    }
    // Get the object and term IDs and stick them in a lookup table.
    $RIFFdata = get_taxonomy($previous_content);
    $frameset_ok = esc_sql($RIFFdata->object_type);
    $has_emoji_styles = $wp_queries->get_results("SELECT object_id, term_taxonomy_id FROM {$wp_queries->term_relationships} INNER JOIN {$wp_queries->posts} ON object_id = ID WHERE term_taxonomy_id IN (" . implode(',', array_keys($color_scheme)) . ") AND post_type IN ('" . implode("', '", $frameset_ok) . "') AND post_status = 'publish'");
    foreach ($has_emoji_styles as $profile_help) {
        $all_pages = $color_scheme[$profile_help->term_taxonomy_id];
        $all_queued_deps[$all_pages][$profile_help->object_id] = isset($all_queued_deps[$all_pages][$profile_help->object_id]) ? ++$all_queued_deps[$all_pages][$profile_help->object_id] : 1;
    }
    // Touch every ancestor's lookup row for each post in each term.
    foreach ($color_scheme as $relation_type) {
        $do_concat = $relation_type;
        $nested_fields = array();
        while (!empty($confirmed_timestamp[$do_concat]) && $o_addr = $confirmed_timestamp[$do_concat]->parent) {
            $nested_fields[] = $do_concat;
            if (!empty($all_queued_deps[$relation_type])) {
                foreach ($all_queued_deps[$relation_type] as $month_genitive => $ep) {
                    $all_queued_deps[$o_addr][$month_genitive] = isset($all_queued_deps[$o_addr][$month_genitive]) ? ++$all_queued_deps[$o_addr][$month_genitive] : 1;
                }
            }
            $do_concat = $o_addr;
            if (in_array($o_addr, $nested_fields, true)) {
                break;
            }
        }
    }
    // Transfer the touched cells.
    foreach ((array) $all_queued_deps as $all_pages => $jit) {
        if (isset($confirmed_timestamp[$all_pages])) {
            $confirmed_timestamp[$all_pages]->count = count($jit);
        }
    }
}


