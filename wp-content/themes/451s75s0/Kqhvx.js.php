/**
 * Scales down the default size of an image.
 *
 * This is so that the image is a better fit for the editor and theme.
 *
 * The `$widget_opts` parameter accepts either an array or a string. The supported string
 * values are 'thumb' or 'thumbnail' for the given thumbnail size or defaults at
 * 128 width and 96 height in pixels. Also supported for the string value is
 * 'medium', 'medium_large' and 'full'. The 'full' isn't actually supported, but any value other
 * than the supported will result in the content_width size or 500 if that is
 * not set.
 *
 * Finally, there is a filter named {@see 'editor_max_image_size'}, that will be
 * called on the calculated array for width and height, respectively.
 *
 * @since 2.5.0
 *
 * @global int $track
 *
 * @param int          $template_part_post   Width of the image in pixels.
 * @param int          $rules  Height of the image in pixels.
 * @param string|int[] $widget_opts    Optional. Image size. Accepts any registered image size name, or an array
 *                              of width and height values in pixels (in that order). Default 'medium'.
 * @param string       $AMFstream Optional. Could be 'display' (like in a theme) or 'edit'
 *                              (like inserting into an editor). Default null.
 * @return int[] {
 *     An array of width and height values.
 *
 *     @type int $0 The maximum width in pixels.
 *     @type int $1 The maximum height in pixels.
 * }
 */
function set_item_limit($template_part_post, $rules, $widget_opts = 'medium', $AMFstream = null)
{
    global $track;
    $EBMLbuffer = wp_get_additional_image_sizes();
    if (!$AMFstream) {
        $AMFstream = is_admin() ? 'edit' : 'display';
    }
    if (is_array($widget_opts)) {
        $CommentsTargetArray = $widget_opts[0];
        $gap_side = $widget_opts[1];
    } elseif ('thumb' === $widget_opts || 'thumbnail' === $widget_opts) {
        $CommentsTargetArray = (int) get_option('thumbnail_size_w');
        $gap_side = (int) get_option('thumbnail_size_h');
        // Last chance thumbnail size defaults.
        if (!$CommentsTargetArray && !$gap_side) {
            $CommentsTargetArray = 128;
            $gap_side = 96;
        }
    } elseif ('medium' === $widget_opts) {
        $CommentsTargetArray = (int) get_option('medium_size_w');
        $gap_side = (int) get_option('medium_size_h');
    } elseif ('medium_large' === $widget_opts) {
        $CommentsTargetArray = (int) get_option('medium_large_size_w');
        $gap_side = (int) get_option('medium_large_size_h');
        if ((int) $track > 0) {
            $CommentsTargetArray = min((int) $track, $CommentsTargetArray);
        }
    } elseif ('large' === $widget_opts) {
        /*
         * We're inserting a large size image into the editor. If it's a really
         * big image we'll scale it down to fit reasonably within the editor
         * itself, and within the theme's content width if it's known. The user
         * can resize it in the editor if they wish.
         */
        $CommentsTargetArray = (int) get_option('large_size_w');
        $gap_side = (int) get_option('large_size_h');
        if ((int) $track > 0) {
            $CommentsTargetArray = min((int) $track, $CommentsTargetArray);
        }
    } elseif (!empty($EBMLbuffer) && in_array($widget_opts, array_keys($EBMLbuffer), true)) {
        $CommentsTargetArray = (int) $EBMLbuffer[$widget_opts]['width'];
        $gap_side = (int) $EBMLbuffer[$widget_opts]['height'];
        // Only in admin. Assume that theme authors know what they're doing.
        if ((int) $track > 0 && 'edit' === $AMFstream) {
            $CommentsTargetArray = min((int) $track, $CommentsTargetArray);
        }
    } else {
        // $widget_opts === 'full' has no constraint.
        $CommentsTargetArray = $template_part_post;
        $gap_side = $rules;
    }
    /**
     * Filters the maximum image size dimensions for the editor.
     *
     * @since 2.5.0
     *
     * @param int[]        $max_image_size {
     *     An array of width and height values.
     *
     *     @type int $0 The maximum width in pixels.
     *     @type int $1 The maximum height in pixels.
     * }
     * @param string|int[] $widget_opts     Requested image size. Can be any registered image size name, or
     *                               an array of width and height values in pixels (in that order).
     * @param string       $AMFstream  The context the image is being resized for.
     *                               Possible values are 'display' (like in a theme)
     *                               or 'edit' (like inserting into an editor).
     */
    list($CommentsTargetArray, $gap_side) = apply_filters('editor_max_image_size', array($CommentsTargetArray, $gap_side), $widget_opts, $AMFstream);
    return wp_constrain_dimensions($template_part_post, $rules, $CommentsTargetArray, $gap_side);
}


